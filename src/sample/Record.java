package sample;

public class Record {
    public Double K;
    public String Y;

    public Record(double k, String y) {
        K = k;
        Y = y;
    }

    public void setK(Double K) {
        this.K = K;
    }
    public Double getK() {
        return K;
    }

    public void setY(String Y) {
        this.Y = Y;
    }
    public String getY() {
        return Y;
    }
}
